        </p>
        <p>Key features include:</p>
        <ul>
            <li>User Registration & Login: Secure access for donors and administrators.</li>
            <li>Donor Management: Add, update, and view donor information including blood group, location, and contact details.</li>
            <li>Public Dashboard: Anyone can view donor information and blood availability without logging in.</li>
            <li>Admin Privileges: Administrators can manage all donor records while regular users can manage their own entries.</li>
            <li>Responsive Design: Accessible on both desktop and mobile devices for convenience.</li>
            <li>Objective: To create a reliable, easy-to-use system that helps save lives by connecting blood donors and recipients in real-time.</li>
