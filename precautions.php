    <br><br><br><h2 style="text-align:center;">Precautions Before & After Donation</h2>
    <div class="precautions-list">
    <h2 style="text-align:left;"> Before Donating Blood</h2>

    <h3> Health & Eligibility:</h3>
    <ul>
        <li>Ensure you are <b>18–60 years old</b> and weigh at least <b>50 kg</b>.</li>
        <li>Do not donate if you have <b>fever, cold, flu, infection, or recent surgery</b>.</li>
        <li>Avoid donating if you recently got <b>vaccinated</b> or took <b>antibiotics</b> (wait <b>2–4 weeks</b>).</li>
        <li>If you had <b>tattooing, piercing, or dental procedures</b>, wait at least <b>6 months</b>.</li>
    </ul>

    <h3> On the Day of Donation:</h3>
    <ul>
        <li>Get a <b>good night’s sleep</b> before donating.</li>
        <li>Eat a <b>healthy meal</b> at least <b>2–3 hours</b> before donation (avoid oily or fatty food).</li>
        <li>Drink plenty of <b>water or juice</b> (500 ml – 1 liter) before donating.</li>
        <li>Avoid <b>alcohol, smoking, and caffeine</b> for at least <b>24 hours</b> before donation.</li>
        <li>Carry a <b>valid ID proof</b> and inform staff if you are on any <b>medication</b>.</li>
    </ul>

    <h3> During Donation:</h3>
    <ul>
        <li>Stay <b>relaxed and calm</b>. Do not cross your legs while donating.</li>
        <li>Inform the staff immediately if you feel <b>dizzy, nauseous, or weak</b>.</li>
    </ul>
</div>

<hr style="margin: 30px 0; border: 1px solid #ddd;">

<div class="precautions-list">
    <h2 style="text-align:left;"> After Receiving Blood (Transfusion)</h2>

    <h3> Immediately After Transfusion:</h3>
    <ul>
        <li><b>Rest</b> for at least <b>30 minutes</b> and avoid strenuous activity.</li>
        <li>Monitor for <b>allergic reactions</b>: itching, rash, chills, fever, or breathing difficulty — inform your doctor immediately if they occur.</li>
        <li><b>Stay hydrated</b> — drink water or juice to help your body adjust.</li>
        <li>Keep the <b>injection site clean and dry</b> for 24 hours.</li>
    </ul>

    <h3> Next 24–48 Hours:</h3>
    <ul>
        <li>Avoid <b>heavy exercise, smoking, and alcohol</b>.</li>
        <li>Eat <b>iron-rich foods</b> (like leafy greens, red meat, or beans) to support blood production.</li>
        <li>Watch for signs like <b>fever, yellowing eyes/skin, dark urine, or swelling</b> — report these to a healthcare professional immediately.</li>
        <li>Attend any <b>follow-up appointments</b> or blood tests as advised by your doctor.</li>
    </ul>
